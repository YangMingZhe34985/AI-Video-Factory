from pathlib import Path

from app.adapters.base import BaseModelAdapter
from app.services.storage_service import MOCK_PNG_BYTES
from app.utils.files import ensure_dir
from app.utils.ids import new_id


class MockAdapter(BaseModelAdapter):
    output_kind = "json"

    def build_payload(self, inputs: dict, params: dict) -> dict:
        self.validate_inputs(inputs, params)
        return {
            "model": self.model_id,
            "inputs": inputs,
            "parameters": params,
        }

    def submit(self, payload: dict) -> dict:
        return {
            "provider_task_id": new_id("mock_task"),
            "status": "submitted",
            "payload_echo": payload,
        }

    def poll(self, task_id: str) -> dict:
        return {
            "provider_task_id": task_id,
            "status": "success",
            "output_kind": self.output_kind,
            "artifacts": [],
        }

    def download_outputs(self, response: dict, output_dir: str) -> list:
        output_path = ensure_dir(output_dir)
        kind = response.get("output_kind") or self.output_kind
        expected = response.get("expected_output_path")
        if kind == "image":
            target = Path(expected) if expected else output_path / "first_frame.png"
            target.write_bytes(MOCK_PNG_BYTES)
        elif kind == "video":
            target = Path(expected) if expected else output_path / "video.mp4"
            target.write_bytes(b"Mock video bytes generated by MockAdapter.\n")
        else:
            target = Path(expected) if expected else output_path / "response.json"
            target.write_text("{}", encoding="utf-8")
        return [str(target)]

    def parse_artifacts(self, response: dict) -> list:
        return response.get("artifacts") or []


class MockVideoAdapter(MockAdapter):
    output_kind = "video"


class MockImageAdapter(MockAdapter):
    output_kind = "image"


class MockChatAdapter(MockAdapter):
    output_kind = "json"

    def submit(self, payload: dict) -> dict:
        response = super().submit(payload)
        response["json"] = self._mock_json(payload)
        response["content"] = self._mock_content(payload, response["json"])
        response["message"] = {
            "content": response["content"],
            "inputs": payload.get("inputs", {}),
        }
        return response

    def download_outputs(self, response: dict, output_dir: str) -> list:
        target_dir = ensure_dir(output_dir)
        target = Path(target_dir) / "chat_response.json"
        target.write_text(str(response), encoding="utf-8")
        return [str(target)]

    @staticmethod
    def _mock_json(payload: dict) -> dict:
        inputs = payload.get("inputs") or {}
        schema = inputs.get("response_schema") or {}
        required = schema.get("required") or []
        if "t2v" in required:
            return {
                "t2v": (
                    "Cinematic vertical short video prompt. Keep the main subject clear, "
                    "lighting soft, movement natural, and camera motion stable."
                )
            }
        if "first_frame_image" in required or "i2v" in required:
            t2v_text = inputs.get("t2v_prompt") or inputs.get("user_prompt") or ""
            return {
                "first_frame_image": (
                    "High quality first-frame image prompt with the subject centered, "
                    "consistent wardrobe, pose, lighting, and scene mood."
                ),
                "i2v": (
                    f"{str(t2v_text).strip() or 'Generate a natural video from the first frame.'}\n"
                    "Use the first frame as the starting image, preserve identity and composition, "
                    "and animate with smooth motion."
                ),
            }
        if "r2v_flash" in required:
            t2v_text = inputs.get("t2v_prompt") or inputs.get("user_prompt") or ""
            return {
                "r2v_flash": (
                    f"{str(t2v_text).strip() or 'Generate dynamic video from references.'}\n"
                    "Preserve reference identity, facial details, wardrobe, pose, and visual style."
                )
            }
        if "full_i2i_prompt" in required:
            t2i_text = inputs.get("t2i") or inputs.get("t2i_prompt") or inputs.get("first_frame_image") or ""
            return {
                "full_i2i_prompt": (
                    f"{str(t2i_text).strip() or 'Use the input portrait as visual reference.'}\n"
                    "Preserve identity, body shape, wardrobe, facial details, lighting, and camera composition. "
                    "Convert the text-to-image prompt into an image-to-image prompt suitable for batch testing."
                )
            }
        return {}

