                "整体风格：写实短视频风格，画面干净稳定。\n\n"
                "## 人物与造型\n"
                "成年人物设定：成年年轻人物，气质自然。\n"
                "五官：面部特征清晰，表情稳定。\n"
                "发型：发丝自然，随动作轻微联动。\n"
                "体态：站姿自然，动作流畅。\n"
                "服装：服装材质清晰，颜色与场景协调。\n\n"
                "## 场景与光影\n"
                "场景：单场景空间，主体突出。\n"
                "背景元素：背景简洁，环境元素服务主体。\n"
                "光线方向：柔和自然光包裹主体。\n"
                "色彩氛围：自然色调，整体观感统一。\n\n"
                "## 运镜&动作规则\n"
